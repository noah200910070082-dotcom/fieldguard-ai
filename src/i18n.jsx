const I18nContext = createContext(null)

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => {
    try { return localStorage.getItem('fieldguard-lang') || 'zh-TW' } catch { return 'zh-TW' }
  })

  const t = useCallback((key, ...args) => {
    let text = T[lang]?.[key] ?? T['zh-TW']?.[key] ?? key
    args.forEach((arg, i) => { text = text.replace(`{${i}}`, arg) })
    return text
  }, [lang])

  const setLanguage = useCallback((l) => {
    setLang(l)
    try { localStorage.setItem('fieldguard-lang', l) } catch {}
  }, [])

  return (
    <I18nContext.Provider value={{ lang, setLanguage, t, T }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used within I18nProvider')
  return ctx
}

